//                              Introduction of JavaScript (Short)
// *DEFINATION 
// JavaScript is a programming language used to make web pages interactive.
// It runs in the browser and is used for logic, events, and dynamic content like forms, buttons, and animations.




// *Basic Syntax of JavaScript

// In JavaScript, statements are usually terminated with semicolons (;).
// Comments are used to explain the code and improve readability. 
// Single-line comments use //,//  
// while multi-line comments are written inside /* */.

// Variables are declared using let, const, or var.
// Functions can be defined using the function keyword or by using arrow functions (=>).




// *Additional Important Points
// JavaScript is case-sensitive.
// Code is executed from top to bottom.
// const is used when the value should not be changed.
// Semicolons are optional, but they are recommended for clarity.